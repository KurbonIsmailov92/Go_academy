Задача 2
Вычислить суммарную зарплату для каждой должности, где средняя зарплата
превышает 50 000
 */

SELECT e.department  AS "Должность",
       SUM(e.salary) AS "Сумма зарплат"
FROM employees e
GROUP BY e.department
HAVING AVG(e.salary) > 50000;
